package edu.sbillysusanto.java.week2;

public class Loop {

	public static void main(String[] args) {
		
		// counter is a variable
		
		int counter = 0;
		
		// the syntax for the loop is just "while"
		
		
		while (counter < 20) {
			
			// "counter < 20" is a condition that the loop will follow
			
			System.out.println(counter);
			
			counter++;
			
			// I have to add another command to end the loop or else it would just loop counter < 20 , FOREVER. Which is a lot of zero's.
			
			// counter++ means that one will be added to the variable until the condition to stop the loop is achieved.

		}
	}

}
