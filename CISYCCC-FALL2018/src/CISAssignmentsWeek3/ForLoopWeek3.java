package CISAssignmentsWeek3;

public class ForLoopWeek3 {

	public static void main(String[] args) {
		
		for(int i = 50; i>25; i--) {
			// int i = 50 is the initial expression
			// i>25 is the condition (boolean expression)
			// i- is the decrement operation
			System.out.println("The value of i is : " +i);
		}
	}
	
}
